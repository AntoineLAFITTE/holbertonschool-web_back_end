#!/usr/bin/env python3
"""Type-annotated function that returns the floor of the float"""


def floor(n: float) -> int:
    """
    Takes a float n and returns the floor of the float as an integer.

    Parameters:
    n: a float wich its floor value is to be returned.

    Returns:
    int: The largest integer less than or equal to n.
    """
    return int(n)
