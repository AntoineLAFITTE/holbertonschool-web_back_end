#!/usr/bin/env python3
"""Add function with type anotation"""


def add(a: float, b: float) -> float:
    """
    Takes 2 floats and return their sum as a float

    Parameters:
    a (float): first number to add.
    b (float): second number to add

    Returns:
    floats: The sum of 'a" and 'b'.
    """

    return a + b
